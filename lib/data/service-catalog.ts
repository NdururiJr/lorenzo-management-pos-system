/**
 * Get service items filtered by category
 */
export function getServicesByCategory(categoryName: string): ServiceItem[] {
  if (categoryName === 'All Services') {
    return SERVICE_ITEMS.filter((item) => item.available);
  }
  return SERVICE_ITEMS.filter(
    (item) => item.category === categoryName && item.available
  );
}

/**
 * Search service items by name or type
 */
export function searchServices(query: string): ServiceItem[] {
  const lowerQuery = query.toLowerCase();
  return SERVICE_ITEMS.filter(
    (item) =>
      item.available &&
      (item.name.toLowerCase().includes(lowerQuery) ||
        item.type.toLowerCase().includes(lowerQuery))
  );
}

/**
 * Get a service item by ID
 */
export function getServiceById(id: string): ServiceItem | undefined {
  return SERVICE_ITEMS.find((item) => item.id === id);
}

/**
 * Filter and search services by category and query
 */
export function filterServices(
  categoryName: string,
  searchQuery: string
): ServiceItem[] {
  let items = getServicesByCategory(categoryName);

  if (searchQuery.trim()) {
    const lowerQuery = searchQuery.toLowerCase();
    items = items.filter(
      (item) =>
        item.name.toLowerCase().includes(lowerQuery) ||
        item.type.toLowerCase().includes(lowerQuery)
    );
  }

  return items;
}

/**
 * Get a price for a specific service type from a service item
 */
export function getServicePrice(
  item: ServiceItem,
  serviceType: keyof ServicePricing
): number {
  return item.pricing[serviceType] || 0;
}

/**
 * Get all available service types for an item
 */
export function getAvailableServices(item: ServiceItem): (keyof ServicePricing)[] {
  const available: (keyof ServicePricing)[] = [];
  if (item.pricing.dryClean) available.push('dryClean');
  if (item.pricing.pressing) available.push('pressing');
  if (item.pricing.laundry) available.push('laundry');
  if (item.pricing.repairs) available.push('repairs');
  return available;
}

/**
 * Calculate total price for multiple services
 */
export function calculateTotalPrice(
  item: ServiceItem,
  selectedServices: (keyof ServicePricing)[]
): number {
  return selectedServices.reduce((total, service) => {
    return total + (item.pricing[service] || 0);
  }, 0);
}
