/**
 * Service Catalog Data Structure
 *
 * Predefined service items that combine garment type + service into quick-add cards.
 * Used by the POS system for visual browsing and quick order entry.
 *
 * @module lib/data/service-catalog
 */

export interface ServicePricing {
  dryClean?: number;      // Column 6: Dry Cleaning price
  pressing?: number;      // Column 10: Pressing Only price
  laundry?: number;       // Column 11: Laundry price
  repairs?: number;       // Column 5: Repairs & Alteration price
}

export interface ServiceItem {
  id: string;
  name: string;
  type: string;           // Garment type (e.g., Shirt, Suit, Dress)
  turnaround: string;     // Expected completion time
  category: string;       // Category for filtering
  icon: string;           // Emoji icon
  available: boolean;     // Whether item is currently available
  pricing: ServicePricing; // Multi-service pricing from Adlife
  // Legacy fields (deprecated - being migrated)
  serviceType?: string;   // @deprecated Use pricing object instead
  services?: string[];    // @deprecated Use pricing object instead
  price?: number;         // @deprecated Use pricing object instead
}

export interface ServiceCategory {
  id: string;
  name: string;
  icon: string;
  color: string;          // Hex color for category styling
}

